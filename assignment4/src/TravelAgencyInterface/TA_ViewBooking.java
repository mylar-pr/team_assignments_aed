     public void populate_Table() {
        DefaultTableModel TBLmod = (DefaultTableModel) tbl_Bookings.getModel();

        for (Booking b : booking_dir.getBookingList()) {

            if (b.getBookedBy().equalsIgnoreCase("TravelAgent")) {
                
                Object row[] = new Object[7];
                row[0] = b.getFullName();
                row[1] = b.getAirlinerName();
                row[2] = b.getFlightID();
                row[3] = b.getBookedSeats();
                row[4] = b.getSource();
                row[5] = b.getDestination();
                row[6] = b;
                
                

                TBLmod.addRow(row);
            }
        }
    }
